<h3><span style="color:red;font-weight:bold">游戏（小说）背景</span></h3>
<em>
《地铁2033》讲述的是由于大面积的核战，整个世界已经是一片废墟，人类几乎全灭，只是当年乘坐莫斯科地铁的人才得以生存，由于高层充满了辐射城市已经不适合人类居住，森林变成了无尽的荒野和沙漠。仍存活的人怀念着美好的过去，但残余的文明逐渐沦为记忆，成为了永远的传说和故事。VDNH是地下铁北部尽头的一个居住地，那里由最强壮的人来守卫整个地铁的安全。不过却突然遭受到一群被称为“DARKENS”神秘生物的袭击，虽然人们奋力抵抗，但似乎是越来越困难了，全灭只是时间问题。Artyom是住在VDNH的一个年轻人，他接到任务要求救于地下铁的中心——传奇POLIS，以提醒所有的人人类再次受到严重威胁，在他的手中是VDHN的命运，是整个地下都市的命运，也可能是整个人类的命运。
从游戏剧情上看，《地铁2033》毫无疑问是一款恐怖生存FPS游戏，游戏剧情的发展除了主线任务之外，游戏还设定了多种多样的非强制性的分支任务，游戏的自由度颇高，不同的政治倾向也会让玩家直接影响游戏的剧情，玩家将会体验到多种不同的故事结局。《地铁2033》并不是一款纯粹的FPS游戏，除了射击要素外，游戏融合了冒险、动作、RPG等多重元素和风格，玩家在游戏中可能扮演一个不起眼的杀手，亦或是一个极具攻击性的战斗机器，但无论怎样，作为玩家必将认真遵循游戏世界的每一个事件，铲除阴谋，找出事情的真相。
游戏人物模型
游戏人物模型
游戏中所设定的是一个极为动态的世界，莫斯科地下铁车站救助了侥幸生存的人们，但似乎人类欲望的根本永远无法磨灭，于是车站也被划分为不同政治派别上的政权小都市。在那里玩家不仅要面对不同的政治制度，像是民主与市场经济，极权派系、军事化的共产主义、文化和信仰等等，还要面对各种以新的智能生命形式出现的敌人，玩家所要进行的不仅仅是火爆激烈的战斗，还包括探索未知的领域，了解核污染后的新纪元世界及其规律，诸如：各个集团及其关联性、贸易、敌人的奇异能力等等。同样的，相较于新智能形的敌人来说，游戏也会为玩家提供各式最新的武器，这些武器将涵盖常见的武器，或是可以用于组装等等武器。游戏采用了4A引擎，该引擎是基于人类真实物理光学感受，大量运用了热扭变、光折射、水纹扭曲，还有Gamma折射阴影渲染技术、HDR、DOF和动态模糊等等特效，而游戏中对于光照效果的运用采取了数以万计光源的全局动态光照效果，包括阳光和天空。
景深效果
景深效果
作为带给人类光明的新城莫斯科地铁，对于场景环境的设计上，制作小组也是作了精心的刻画，无论是对地下隧道的描绘，还是地面建筑物的刻画，制作人员都遵循了一个原则，那就是极力表现世界末日后颠覆性的灾难所带来的破坏。在色彩的运用上，游戏采用了大量的灰色调，坍塌毁损的建筑物笼罩到灰色的天空下，映入眼帘的是一座座废墟之城，茂密苍翠的森林已经不复存在，荒凉凄惨的场景触目惊心，就连空气也变得浑浊不清，而地下的车站和隧道昏暗和萧条预示着巨大灾难带来的不仅仅是满目疮痍，还有心灵与精神上的创伤。
除了哀伤的色彩氛围以外，游戏也从另一方面表达了人类渴求生存的希望，地下铁车站平和生活的民众、他们其中有的属于中立，他们会去特定的地方工作，譬如：忙于烹调、缝纫的女人们，还有忙着做不同生意的人们以及玩耍的孩子们，而男人们则清洁着武器、讨论事情、看书或是喝酒喝茶，年老体衰的老人们则安详的坐在帐篷周围。赋予了游戏中的每一个敌人，包括NPCs在内高级的AI，他们每个人的行动或战斗都非常的逼真，特别是在战斗中，敌人或尽可能的同现实战斗一样行动，他们不仅会避免被射击，还会会视情况作出直截了当的瞄准目标开火，或是掩藏在掩体后，埋伏或突袭玩家，要求支援，亦或是积极的搜寻角落，寻找隐藏的目标。
为了增加游戏的可玩性，游戏中还加入了丰富多样的迷你游戏，游戏的血腥暴力成份也是高级别的，惩罚以死亡为代价，当玩家消灭侵略的敌人时，这些新的生物都会受到地心引力的变形，整个人物看上去像是腐烂一般。</em>

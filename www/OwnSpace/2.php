<h3><span style="color:red;font-weight:bold">编程入门0基础，我应该学什么？</span></h3>
<em>
个人推荐入门C语言或者python :<br>
　　C语言语法简单，有良好的逻辑抽象，入门的话还是先面向过程吧，不要刚学就面向对象云里雾里的，如果是要更全面、深入的学习语言本身就C.<br>
　　python语法更加简单简洁易学，而且有各种强大的库，扩展库，可过程可对象，如果是为了通过编程这一手段来迅速的做出有用、实用的程序和界面可以学python.<br>
　　入门是从Basic语言开始的，大概熟悉程序的基础语法与逻辑后，进阶C语言。<br>
